public class IfElse01 {
    public static void main(String[] args) {
        int money = 6000;
        if (money >= 8000) {
            System.out.println("순대국 마시쪙!");
        } else if(money >= 5000) {
            System.out.println("한우 스테이크 마시쪙!");
        } else {
            System.out.println("흑흑흑, 배고프당...");
        }
    }
}
